package com.example.Model;

public class Job {
    private int id;
    private String name;

    // Getter và setter cho trường 'id'
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter và setter cho trường 'name'
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
